    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        int count = jcLanguage.getSelectedIndex();
        if(count==0) {
            JOptionPane.showMessageDialog(null, "Please choose a subject first!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        count = jcExamId.getItemCount();
        if(count==0) {
            JOptionPane.showMessageDialog(null, "Sorry! No paper is available for "+subjectName+" subject.", "No Paper available!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String examId = jcExamId.getSelectedItem().toString();
        subjectName = jcLanguage.getSelectedItem().toString();
        int ans = JOptionPane.showConfirmDialog(null, "You have selected:\nSubject: "+subjectName+"\nExam: "+examId+"\nDo you want to proceed?", "Confirmation!", JOptionPane.YES_NO_OPTION);
        if(ans == JOptionPane.YES_OPTION) {
            try {
                int totalQues = ExamDAO.getQuestionCountByExam(examId);
                ExamPojo exam = new ExamPojo(examId, subjectName, totalQues);
                new TakeTestFrame(exam).setVisible(true);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

