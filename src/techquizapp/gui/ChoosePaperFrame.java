    private void jcLanguageItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcLanguageItemStateChanged
        if(evt.getStateChange()==ItemEvent.DESELECTED)
            return;
        if(!validateInput()) {
            JOptionPane.showMessageDialog(null, "Please choose a subject first!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        jcExamId.removeAllItems();
        try {
            if(!ExamDAO.isPaperSet(subjectName)) {
                JOptionPane.showMessageDialog(null, "Sorry! No paper is available for "+subjectName+" subject.", "No Paper available!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ArrayList<String> examIdList = ExamDAO.getExamIdBySubject(UserProfile.getUsername(), subjectName);
            if(examIdList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have appeared for all the exams for "+subjectName+"!", "No Exams left!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            for(String s : examIdList) {
                jcExamId.addItem(s);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcLanguageItemStateChanged

