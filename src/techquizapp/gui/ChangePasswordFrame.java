    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int input = validateInputs();
        switch(input){
            case 0:
                JOptionPane.showMessageDialog(null, "Please fill all the fields!!", "Error!!", JOptionPane.ERROR_MESSAGE);
                return;
            case -1:
                JOptionPane.showMessageDialog(null, "Password should contain minimum 4 characters!", "Error!!", JOptionPane.ERROR_MESSAGE);
                return;
            case -2:
                JOptionPane.showMessageDialog(null, "Passwords do not match!!", "Error!!", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        UserPojo student = new UserPojo(userId, password, "Student");
        try {
            UserDAO.changePassword(student);
            JOptionPane.showMessageDialog(null, "Password successfully changed!", "Success!!", JOptionPane.INFORMATION_MESSAGE);
            txtPassword.setText("");
            txtRePassword.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB!", "Error!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

