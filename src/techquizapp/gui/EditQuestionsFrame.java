    private void displayQuestion(int pos) {
        ques = qstore.getQuestion(pos);
        lblQno.setText("Question No.: "+ques.getQno());
        txtQuestion.setText(ques.getQuestion());
        txtOption1.setText(ques.getAnswer1());
        txtOption2.setText(ques.getAnswer2());
        txtOption3.setText(ques.getAnswer3());
        txtOption4.setText(ques.getAnswer4());
        jcCorrectAnswer.setSelectedItem(options.get(ques.getCorrectAnswer()));
    }
    
    private void addQuestion(int pos) {
        qstore.removeQuestion(pos);
        ques.setQuestion(txtQuestion.getText());
        ques.setAnswer1(txtOption1.getText());
        ques.setAnswer2(txtOption2.getText());
        ques.setAnswer3(txtOption3.getText());
        ques.setAnswer4(txtOption4.getText());
        ques.setCorrectAnswer(options.get(jcCorrectAnswer.getSelectedItem()));
        qstore.setQuesionAt(pos, ques);
    }
