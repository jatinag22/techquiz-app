    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        addQuestion(pos);
        if(JOptionPane.showConfirmDialog(null, "Are you sure?", "Confirm?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                QuestionDAO.updateQuestions(qstore);
                JOptionPane.showMessageDialog(null, "Successfully added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                new AdminOptionsFrame().setVisible(true);
                dispose();
            } catch(SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error while updating questions in the Database!", "Exception!", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDoneActionPerformed

